CREATE TABLE IF NOT EXISTS "corridor_locations" (
	"id"	INT,
	"name"	TEXT NOT NULL UNIQUE,
	PRIMARY KEY("id")
);
CREATE TABLE IF NOT EXISTS "passengers" (
	"id"	INT,
	"first_name"	TEXT NOT NULL,
	"last_name"	TEXT NOT NULL,
	"gender"	TEXT NOT NULL CHECK("gender" IN ('M', 'F', 'O')),
	"email_address"	TEXT NOT NULL UNIQUE,
	"address"	TEXT NOT NULL,
	PRIMARY KEY("id")
);
CREATE TABLE IF NOT EXISTS "drivers" (
	"id"	INT,
	"first_name"	TEXT NOT NULL,
	"last_name"	TEXT NOT NULL,
	"gender"	TEXT NOT NULL CHECK("gender" IN ('M', 'F', 'O')),
	"email_address"	TEXT NOT NULL UNIQUE,
	"address"	TEXT NOT NULL,
	"date_of_birth"	TEXT,
	"national_identity_number"	TEXT NOT NULL UNIQUE,
	PRIMARY KEY("id")
);
CREATE TABLE IF NOT EXISTS "vehicles" (
	"id"	INT,
	"vin"	TEXT NOT NULL UNIQUE,
	"plate_number"	TEXT NOT NULL UNIQUE,
	"model"	TEXT NOT NULL,
	"capacity"	INT NOT NULL,
	"status"	TEXT NOT NULL CHECK("status" IN ('Available', 'In use', 'Under maintenance', 'Out of use')),
	PRIMARY KEY("id")
);
CREATE TABLE IF NOT EXISTS "passenger_cards" (
	"id"	INT,
	"card_number"	TEXT NOT NULL UNIQUE,
	"issue_date"	TEXT,
	"phone_number"	TEXT NOT NULL UNIQUE,
	"passenger_id"	INT NOT NULL,
	PRIMARY KEY("id"),
	FOREIGN KEY("passenger_id") REFERENCES "passengers"("id") ON DELETE CASCADE ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS "trips" (
	"id"	INT,
	"departure_location"	TEXT NOT NULL,
	"arrival_location"	TEXT NOT NULL,
	"departure_time"	TEXT NOT NULL,
	"arrival_time"	TEXT NOT NULL,
	"driver_id"	INT NOT NULL,
	"trip_capacity"	INT NOT NULL,
	PRIMARY KEY("id"),
	FOREIGN KEY("driver_id") REFERENCES "drivers"("id") ON UPDATE CASCADE ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS "passenger_trips" (
	"id"	INT,
	"passenger_id"	INT NOT NULL,
	"trip_id"	INT NOT NULL,
	"price"	REAL NOT NULL,
	PRIMARY KEY("passenger_id","trip_id"),
	FOREIGN KEY("passenger_id") REFERENCES "passengers"("id") ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY("trip_id") REFERENCES "trips"("id") ON DELETE RESTRICT
);
CREATE TABLE IF NOT EXISTS "driver_vehicle_logs" (
	"id"	INT,
	"driver_id"	INT NOT NULL,
	"vehicle_id"	INT NOT NULL,
	"pair_date"	TEXT,
	PRIMARY KEY("id"),
	FOREIGN KEY("driver_id") REFERENCES "drivers"("id") ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY("vehicle_id") REFERENCES "vehicles"("id") ON UPDATE CASCADE ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS "driver_identification_cards" (
	"id"	INT NOT NULL UNIQUE,
	"card_no"	TEXT,
	"issue_date"	TEXT,
	"driver_id"	INT NOT NULL UNIQUE,
	PRIMARY KEY("card_no"),
	FOREIGN KEY("driver_id") REFERENCES "drivers"("id") ON UPDATE CASCADE ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS "driver_additional_details" (
	"id"	INT,
	"phone_number"	TEXT NOT NULL UNIQUE,
	"driver_id"	INT NOT NULL,
	PRIMARY KEY("id"),
	FOREIGN KEY("driver_id") REFERENCES "drivers"("id") ON UPDATE CASCADE ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS "license" (
	"id"	INT,
	"license_number"	TEXT NOT NULL UNIQUE,
	"issue_date"	TEXT NOT NULL,
	"expiry_date"	TEXT NOT NULL,
	"license_status"	TEXT,
	"driver_id"	INT UNIQUE,
	PRIMARY KEY("id"),
	FOREIGN KEY("driver_id") REFERENCES "drivers"("id") ON UPDATE CASCADE ON DELETE CASCADE
);
